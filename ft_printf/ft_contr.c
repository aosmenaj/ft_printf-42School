#include "ft_printf.h"

int	ft_contr(int n)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	else if (n == 0)
	{
		write(1, "0", 1);
		return (1);
	}
	else if (n < 0)
	{
		write (1, "-", 1);
		return (1);
	}
	else
		return (0);
}
