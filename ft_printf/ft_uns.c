#include "ft_printf.h"

int	ft_uns(unsigned int n)
{
	int			i;
	char		seq[10];

	i = 0;
	if (n == 0)
	{
		write(1, "0", 1);
		return (1);
	}
	while (n > 0)
	{
		seq[i] = n % 10 + '0';
		n = n / 10;
		i++;
	}
	i--;
	ft_scrivi(i, seq);
	return (i + 1);
}
